def _resolve_target(ref: str) -> str:
    if ref.startswith("env:"):
        return os.environ.get(ref.split(":", 1)[1], "")
    return os.environ.get(ref, ref if ref.startswith("http") or "@" in ref else "")


def notify(args: argparse.Namespace) -> Dict[str, Any]:
    event = {"event": args.event, "severity": args.severity, "title": args.title, "message": args.message, "project": args.project, "ts": now()}
    target = _resolve_target(args.target_ref)
    out = {"event": event, "target_ref": args.target_ref, "sent": False, "mode": "dry-run"}
    if args.send:
        if not target:
            raise SystemExit(f"Target reference {args.target_ref!r} did not resolve to a target value")
        if args.type in {"webhook", "slack"}:
            body = json.dumps(event if args.type == "webhook" else {"text": f"*{args.title}*\n{args.message}", "event": event}).encode()
            req = urllib.request.Request(target, data=body, headers={"Content-Type": "application/json"}, method="POST")
            with urllib.request.urlopen(req, timeout=10) as resp:
                out.update({"sent": True, "status": getattr(resp, "status", None), "mode": "sent"})
        elif args.type == "email":
            msg = EmailMessage(); msg["From"] = args.email_from; msg["To"] = target; msg["Subject"] = args.title; msg.set_content(args.message)
            with smtplib.SMTP(args.smtp_host, args.smtp_port, timeout=10) as smtp:
                smtp.send_message(msg)
            out.update({"sent": True, "mode": "sent"})
    write_json(Path(args.out), {"notification": out, "payload": event})
    return out


