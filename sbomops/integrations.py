def now() -> str:
    return datetime.now(timezone.utc).isoformat()


def ensure_dir(path: Path) -> Path:
    path.mkdir(parents=True, exist_ok=True)
    return path


def write_text(path: Path, text: str) -> Path:
    ensure_dir(path.parent)
    path.write_text(text, encoding="utf-8")
    return path


def write_json(path: Path, data: Any) -> Path:
    return write_text(path, json.dumps(data, indent=2, sort_keys=False) + "\n")


def write_yaml(path: Path, data: Any) -> Path:
    if yaml:
        return write_text(path, yaml.safe_dump(data, sort_keys=False))
    return write_json(path, data)


def load_json(path: str | Path, default: Any = None) -> Any:
    p = Path(path)
    if not p.exists():
        return default
    try:
        return json.loads(p.read_text(encoding="utf-8", errors="replace"))
    except Exception:
        return default


def finding_fingerprint(*parts: str) -> str:
    return hashlib.sha256("|".join(parts).encode()).hexdigest()[:16]


def component_findings(sbom: str | Path) -> List[Dict[str, Any]]:
    p = Path(sbom)
    findings: List[Dict[str, Any]] = []
    try:
        _fmt, comps, _meta = parse_components(p)
        for c in comps:
            name = c.name or c.bom_ref or "unknown"
            version = c.version or ""
            purl = c.purl or ""
            if not version:
                findings.append({
                    "rule_id": "SST-MISSING-VERSION",
                    "level": "warning",
                    "title": "Component missing exact version",
                    "message": f"Component {name} does not declare an exact version.",
                    "component": name,
                    "version": version,
                    "purl": purl,
                    "fingerprint": finding_fingerprint("missing-version", name, purl),
                })
            if version and any(x in version.lower() for x in ["snapshot", "latest", "dev", "*"]):
                findings.append({
                    "rule_id": "SST-UNPINNED-VERSION",
                    "level": "warning",
                    "title": "Component appears unpinned or pre-release",
                    "message": f"Component {name} has version value {version!r}, which may be unpinned or unstable.",
                    "component": name,
                    "version": version,
                    "purl": purl,
                    "fingerprint": finding_fingerprint("unpinned", name, version, purl),
                })
    except Exception as exc:
        findings.append({
            "rule_id": "SST-SBOM-PARSE-ERROR",
            "level": "error",
            "title": "SBOM parsing failed",
            "message": str(exc),
            "component": str(p),
            "version": "",
            "purl": "",
            "fingerprint": finding_fingerprint("parse-error", str(p), str(exc)),
        })
    if not findings:
        findings.append({
            "rule_id": "SST-SBOM-OBSERVED",
            "level": "note",
            "title": "SBOM parsed successfully",
            "message": "No default export findings were generated by local heuristics.",
            "component": p.name,
            "version": "",
            "purl": "",
            "fingerprint": finding_fingerprint("observed", str(p)),
        })
    return findings


def export_sarif(args: argparse.Namespace) -> Dict[str, Any]:
    findings = component_findings(args.sbom) if args.sbom else []
    release = load_json(args.release_decision, {}) if getattr(args, "release_decision", "") else {}
    if release and release.get("decision") in {"warn", "block"}:
        level = "error" if release.get("decision") == "block" else "warning"
        findings.append({
            "rule_id": "SST-RELEASE-DECISION",
            "level": level,
            "title": f"Release decision: {release.get('decision')}",
            "message": "; ".join(release.get("reasons", [])) or "Release decision produced a non-pass result.",
            "component": "release",
            "version": "",
            "purl": "",
            "fingerprint": finding_fingerprint("release-decision", json.dumps(release, sort_keys=True)),
        })
    rules: Dict[str, Dict[str, Any]] = {}
    results: List[Dict[str, Any]] = []
    for f in findings:
        rid = f["rule_id"]
        rules[rid] = {"id": rid, "name": f["title"], "shortDescription": {"text": f["title"]}, "help": {"text": f["message"]}}
        results.append({
            "ruleId": rid,
            "level": f.get("level", "warning"),
            "message": {"text": f.get("message", "")},
            "locations": [{"physicalLocation": {"artifactLocation": {"uri": str(args.sbom or "sbom")}, "region": {"startLine": 1}}}],
            "partialFingerprints": {"sstFinding": f.get("fingerprint", finding_fingerprint(rid, f.get("message", "")))},
            "properties": {"component": f.get("component", ""), "version": f.get("version", ""), "purl": f.get("purl", "")},
        })
    sarif = {
        "$schema": "https://json.schemastore.org/sarif-2.1.0.json",
        "version": "2.1.0",
        "runs": [{
            "tool": {"driver": {"name": "SBOM Security Toolkit", "semanticVersion": "2.5.0", "informationUri": "https://github.com/hellnbak/sbom_security_toolkit", "rules": list(rules.values())}},
            "automationDetails": {"id": getattr(args, "project", "") or "local"},
            "results": results,
            "properties": {"created_at": now(), "source": "sst export sarif"},
        }],
    }
    out = Path(args.out)
    write_json(out, sarif)
    return {"path": str(out), "findings": len(results), "rules": len(rules)}


def export_openvex(args: argparse.Namespace) -> Dict[str, Any]:
    _fmt, comps, _meta = parse_components(Path(args.sbom))
    statements = []
    for c in comps:
        subject = {"name": c.name or c.bom_ref or "unknown"}
        if c.purl:
            subject["identifiers"] = {"purl": c.purl}
        elif c.bom_ref:
            subject["identifiers"] = {"bom-ref": c.bom_ref}
        statements.append({
            "vulnerability": {"name": args.vulnerability or "UNSPECIFIED"},
            "timestamp": now(),
            "products": [{"@id": subject.get("identifiers", {}).get("purl", subject["name"]), "subcomponents": [subject]}],
            "status": args.status,
            "justification": args.justification,
            "impact_statement": args.impact_statement,
            "action_statement": args.action_statement,
        })
    doc = {"@context": "https://openvex.dev/ns/v0.2.0", "@id": f"urn:sst:openvex:{finding_fingerprint(str(args.sbom), now())}", "author": args.author, "timestamp": now(), "version": 1, "statements": statements}
    out = Path(args.out)
    write_json(out, doc)
    return {"path": str(out), "statements": len(statements), "status": args.status}


def export_jira(args: argparse.Namespace) -> Dict[str, Any]:
    issues = []
    for f in component_findings(args.sbom):
        issues.append({"project": args.project_key, "issuetype": args.issue_type, "summary": f"[{f['rule_id']}] {f['title']}: {f.get('component','')}", "description": f.get("message", ""), "labels": ["sbom-security-toolkit", "supply-chain"], "external_fingerprint": f.get("fingerprint"), "component": f.get("component", ""), "purl": f.get("purl", "")})
    out = Path(args.out)
    write_json(out, {"generated_at": now(), "mode": "dry-run", "issues": issues})
    return {"path": str(out), "issues": len(issues), "note": "Dry-run Jira payload only; no network call performed."}


